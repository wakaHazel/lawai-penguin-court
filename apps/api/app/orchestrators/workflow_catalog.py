def _choice(
    choice_id: str,
    label: str,
    effect_key: str,
    next_node_id: str,
    intent: str,
    risk_tip: str,
    emphasis: str = "steady",
) -> WorkflowChoice:
    return WorkflowChoice(
        choice_id=choice_id,
        label=label,
        effect_key=effect_key,
        next_node_id=next_node_id,
        intent=intent,
        risk_tip=risk_tip,
        emphasis=emphasis,
    )


